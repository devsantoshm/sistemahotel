    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
		if (txtNombre.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre");
			txtNombre.requestFocus();
			return;
		}
		if (txtApaterno.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido paterno");
			txtApaterno.requestFocus();
			return;
		}
		if (txtAmaterno.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido materno");
			txtAmaterno.requestFocus();
			return;
		}
		if (txtNumDoc.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Num Doc");
			txtNumDoc.requestFocus();
			return;
		}
		if (txtSueldo.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Sueldo");
			txtSueldo.requestFocus();
			return;
		}
		if (txtLogin.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Login");
			txtLogin.requestFocus();
			return;
		}
		if (txtPassword.getText().length() == 0) {
			JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Password");
			txtPassword.requestFocus();
			return;
		}
		
		vtrabajador dts = new vtrabajador();
		ftrabajador func = new ftrabajador();
		
		dts.setNombre(txtNombre.getText());	
		dts.setApaterno(txtApaterno.getText());
		dts.setAmaterno(txtAmaterno.getText());
		int seleccionado = cboTipoDoc.getSelectedIndex();
		dts.setTipo_documento((String) cboTipoDoc.getItemAt(seleccionado));
		dts.setNum_documento(txtNumDoc.getText());
		dts.setDireccion(txtDireccion.getText());
		dts.setTelefono(txtTelefono.getText());
		dts.setEmail(txtEmail.getText());
		dts.setSueldo(Double.parseDouble(txtSueldo.getText()));
		seleccionado = cboAcceso.getSelectedIndex();
		dts.setAcceso((String) cboAcceso.getItemAt(seleccionado));
		dts.setLogin(txtLogin.getText());
		dts.setPassword(txtPassword.getText());
		seleccionado = cboEstado.getSelectedIndex();
		dts.setEstado((String) cboEstado.getItemAt(seleccionado));
		
		if (accion.equals("guardar")) {
			if (func.insertar(dts)) {
				JOptionPane.showMessageDialog(rootPane, "El trabajador fue registrada correctamente");
				mostrar("");
				inhabilitar();
			}
		}else if (accion.equals("editar")) {
			dts.setIdpersona(Integer.parseInt(txtIdPersona.getText()));
			if (func.editar(dts)) {
				JOptionPane.showMessageDialog(rootPane, "El trabajador fue editado correctamente");
				mostrar("");
				inhabilitar();
			}
		}
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
		if (!txtIdPersona.getText().equals("")) {//es diferente de vacio
			int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¬øEst√°s seguro de eliminar el trabajador?", "Confirmar", 2);
			if (confirmacion == 0) {
				ftrabajador func = new ftrabajador();
				vtrabajador dts = new vtrabajador();
				
				dts.setIdpersona(Integer.parseInt(txtIdPersona.getText()));
				func.eliminar(dts);
				mostrar("");
				inhabilitar();
			}
		}
		
    }//GEN-LAST:event_btnEliminarActionPerformed

