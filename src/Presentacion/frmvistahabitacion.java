    private void tblListHabMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListHabMousePressed
        // TODO add your handling code here:
		if (evt.getClickCount() == 2) {//numero de click con el mouse
			int fila = tblListHab.getSelectedRow();
			String cod;
			String valor;
			
			cod = tblListHab.getValueAt(fila, 0).toString();
			valor = tblListHab.getValueAt(fila, 1).toString();
			
			frmreserva.txtidhabitacion.setText(cod);
			frmreserva.txtnumerohab.setText(valor);
			this.dispose();
		}
    }//GEN-LAST:event_tblListHabMousePressed

